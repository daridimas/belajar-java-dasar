package Komentar;

public class Komentar {
    public static void main(String[] args) {
        /**
         * Method untuk Menjumlahkan value1 + value2
         * @param value1 angka pertama
         * @param value2 angka kedua
         * @return hasil menjumlahkan value1 dan value2
         */

        // Orang-orang java multiline komentar untuk mendokumentasikan method / class

        System.out.println(sum(10, 10));

    }

    static int sum(int value1, int value2) {
        // jumlahkan value1 ditambah value2
        return value1 + value2;
    }

}
