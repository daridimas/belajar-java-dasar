package ExpressionvsStatementvsBlock;

import java.util.Date;

public class Statement {
    public static void main(String[] args) {
        // Statement = kumpulan dari statement / kalimat lengkapnya statement

        // assignment statement
        double  aValue = 8933.234;

        // increment statement
        aValue++;

        // method invocation statement atau memanggil objet atau function
        System.out.println("Hello World!");

        // Object Creation Statement
        Date date = new Date();
    }
}
