package OperasiMatematika;

public class augmentedAssignment {
    public static void main(String[] args) {
        int c = 10;

        c += 10; // artinya c = c + 10

        System.out.println(c);

        c -= 10; // artinya c = c - 10

        System.out.println(c);

        c *= 10; // artinya c = c * 10

        System.out.println(c);
    }
}
